public class For1 {
	/*
      Ejemplo del Bucle For
      Este ejercicio imprime los numeros del 1 al 100
      Comenzando desde el 2

	*/

public static void main(String[] args){
  //Variable
  int i;
  //Bloque del For
  for (i = 2; i<=100; i++)
    //Mostramos resultado por pantalla
    System.out.print(i + " ");
	}
}